
fun main(){

    // EXAMPLE 1. In rage. 0123
    for(i in 1..5){
        println(i)
    }
    println("")

    // EXAMPLE 2. Until. 012
    for(i in 0 until 3)
    {
        println(i)
    }
    println("")

    // EXAMPLE 3. Step. 2468
    for( i in 2..8 step 2)
    {
        println(i)
    }

    println("")

    // EXAMPLE 4. downTo. 3210
    for(i in 3 downTo 0)
    {
        println(i)
    }
    println("")

    // EXAMPLE 5. Rage of characters. abcd
    for(c in 'a'..'d')
    {
        println(c)
    }
    println("")
    // EXAMPLE 6. In Range.

}